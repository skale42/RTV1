int	ft_strcmp(const char *s1, const char *s2)
{
	int i;
	int k;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2 != '\0')
		i++;
	k = (unsigned char)s1[i] - (unsigned char)s2[i];
	return (k);
}
